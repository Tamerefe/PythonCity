if "?" not in g:
	print("? Isareti Kullanin")
else:
	print("Parola Basari Ile Olusturduldu")

if "a" in g :
	print("a Harfi Vardir")
else:
	print("a Harfi Kullanin") 

if "b" in g :
	print("b Harfi Vardir")
else:
	print("b Harfi Kullanin") 

